const add = function addNumbers(num1, num2) {
  return num1 + num2;
};

const subtract = function subtractNumbers(num1, num2) {
  return num1 - num2;
};

const multiply = function multiplyNumbers(num1, num2) {
  return num1 * num2;
};

const divide = function divideNumbers(num1, num2) {
  return num1 / num2;
};
